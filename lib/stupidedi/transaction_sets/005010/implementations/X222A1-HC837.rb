          #
          #   For the purposes of this documentation, the claim detail information
          #   is presented only in the dependent level. Specific claim detail
          #   information can be given in either the subscriber or the dependent
          #   hierarchical level. Because of this, the claim information is said
          #   to "float." Claim information is positioned in the same hierarchical
          #   level that describes its owner-participant, either the subscriber or
          #   the dependent. In other words, the claim information, Loop 2300, is
          #   placed in the hierarchical level when the patient is the subscriber,
          #   or it is placed at the patient/dependent hierarchical level when the
          #   patient is the dependent of the subscriber as shown here. When the
